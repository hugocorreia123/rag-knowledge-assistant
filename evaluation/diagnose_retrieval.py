"""Retrieval-only diagnostic — zero LLM calls.

Runs ONLY the retriever (semantic search) over each test question.
Shows what pages are actually retrieved so we can calibrate the
`expected_pages` in test_questions.yaml before spending tokens on the
full eval.

Run with:
    python -m evaluation.diagnose_retrieval
"""

from __future__ import annotations

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

import yaml

from src.config import TOP_K
from src.retriever import retrieve

TEST_PATH = Path(__file__).parent / "test_questions.yaml"
OUT_PATH = Path(__file__).parent / "retrieval_diagnostic.md"


def main() -> int:
    cases = yaml.safe_load(TEST_PATH.read_text(encoding="utf-8"))

    out = ["# Retrieval diagnostic — zero LLM calls\n"]
    out.append(
        "Generated by `python -m evaluation.diagnose_retrieval`. "
        "Compare each case's `expected_pages` to the pages actually retrieved "
        "to decide whether to update the test set.\n"
    )

    for c in cases:
        if not c.get("should_answer", True):
            continue  # skip out-of-scope; nothing to calibrate

        chunks = retrieve(c["question"], top_k=TOP_K)
        retrieved = [(ch.page, round(ch.similarity, 3)) for ch in chunks]

        expected = c.get("expected_pages", [])
        hit = [p for p, _ in retrieved if p in set(expected)]

        out.append(f"\n## `{c['id']}` — {c['category']}\n")
        out.append(f"**Question:** {c['question']}\n")
        out.append(f"**Expected pages (current):** {expected}\n")
        out.append(
            "**Retrieved (page, sim) top-10:** "
            + ", ".join(f"({p}, {s})" for p, s in retrieved)
            + "\n"
        )
        out.append(f"**Hits:** {hit if hit else 'NONE'}\n")
        # Top chunk preview to help judge whether retrieval is on-topic
        if chunks:
            preview = chunks[0].text[:240].replace("\n", " ")
            out.append(f"**Top chunk preview:** _{preview}_…\n")

    OUT_PATH.write_text("\n".join(out), encoding="utf-8")
    print(f"✓ Wrote {OUT_PATH}")
    print(f"  Open it in VS Code to recalibrate test_questions.yaml.")
    return 0


if __name__ == "__main__":
    sys.exit(main())